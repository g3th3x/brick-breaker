import { Brick } from "./brick.js";

export function stageMaker(game, stage) {
  let bricks = [];

  stage.forEach((row, rowIndex) => {
    row.forEach((brick, brickIndex) => {
      if (brick === 1) {
        let position = {
          x: 15 + 55 * brickIndex,
          y: 40 + 20 * rowIndex,
        };
        bricks.push(new Brick(game, position));
      }
    });
  });

  return bricks;
}

